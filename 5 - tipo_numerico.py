"""
Tipo numérico

Quando se tem dois asteríscos ** nós fazemos
uma operação exponencial.

Se queremos saber se é um número real, inteiro, float, etc...
Printamos type(num)
"""
num = 1_000_000
num += 100
num /= 2
num -= 500_000
num = num - 40
num = num ** 2
num = num / 10
num -= 8
num **= 10
print(num)